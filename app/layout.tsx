import type { Metadata } from "next";
import { Geist, Geist_Mono } from "next/font/google";
import "./globals.css";

const geist = Geist({ subsets: ["latin"], variable: "--font-sans" });
const geistMono = Geist_Mono({ subsets: ["latin"], variable: "--font-mono" });

export const metadata: Metadata = {
  title: "ReviewIQ — Extension review intelligence",
  description: "Scrape Chrome Web Store reviews and analyze them with AI.",
};

// Root layout provides only <html>/<body> + fonts. The sidebar app-shell lives
// in (app)/layout.tsx; the public page lives in (public)/layout.tsx with no shell.
export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  return (
    <html lang="en" className={`${geist.variable} ${geistMono.variable}`}>
      <body>{children}</body>
    </html>
  );
}
